import java.util.Scanner;
public class ForLoops 
{
	public static void main(String[] args) 
	{
		//for loops are used for fixed iteration
		//you know how many times you want to loop ahead of time
		//(by the time you reach the loop within the program)
		
		//basic format:
		//for(declare/initialize counter; condition for counter; increment the counter)
		// {
		//		code
		// }
		
		//output something 10 times
		
		for(int i=0; i < 10; i++)
		{
			System.out.println("Your counter is "+i);
		}
		
	}

}
